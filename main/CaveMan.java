    /**
     * Create the crab world (the beach). Our world has a size 
     * of 5600x5600 cells, where every cell is just 1 pixel.
     */
    public CaveMan() 
    {
        super(20 * 28, 20 * 28, 1);
        GenerateLevel(0);
    }

    public void GenerateLevel(int num)
    {
        Objects = 0;
        int px = TILESIZE/2,
        py = TILESIZE/2;

        removeObjects(getObjects(Block.class));

        for (int i = 0 ; i < 28; i++)
        {     
            for (int j = 0 ; j < 28; j++)
            {
                int ID = LVL_data[num][i][j] ; 

                if (ID == -1)
                {
                    addObject( Player, px, py );
                    Objects++;
                }

                else if(ID > 0)
                {
                    addObject( new Block(ID), px, py );
                    Objects++;
                }
                px += TILESIZE;
            }           
            px = TILESIZE/2;
            py += TILESIZE;
        }

        maploaded = true;
    }

