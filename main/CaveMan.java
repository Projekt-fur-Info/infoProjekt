    {

    }

    public int saveSpotX(int num){
        for (int i = 0 ; i < 28; i++)
        {     
            for (int j = 0 ; j < 28; j++)
            {
                int ID = LVL_data[num][i][j] ;
                if (ID == -2){

                    return i*28;

                }
            }           
        }  
        return 0;
    }

    public int saveSpotY(int num){
        for (int i = 0 ; i < 28; i++)
        {     
            for (int j = 0 ; j < 28; j++)
            {
                int ID = LVL_data[num][i][j] ;
                if (ID == -2){

                    return j*28;

                }
            }           
        }  
        return 0;
    }

    public boolean isSolid(int x, int y, int num)
    {
        //convert cords in tilecords
        x/=TILESIZE;
        y/=TILESIZE;

        if (x < 0 || x >= 28 || y >= 28 ){
            return true;
        }

        switch (LVL_data[num][y][x])
        {
            case 10 :       // Boden

                return true; 

            case 11 :       // alternativer Boden

                return true;

            case 12 :       // tiefer Boden

                return true; 

            case 13 :       // fliegender Boden

                return true;

            case 20 :       // Wand 

                return true;

            case 21 :      // rechte Wand 

                return true;

            case 22 :       // rechter Wandabschluss

                return true;

            case 23 :       // linke Wand 

                return true;

            case 24 :       // linker Wandabschluss

                return true;

            case 80 :       // Block ohne Hitbox

                return true;

            default : return false; 
        }   

    }
